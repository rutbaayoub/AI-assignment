# dictionary with 10 elements and printing values using value() function
fruits_dictionary = {
    "Apple" : 1 ,
    "Banana" : 2 ,
    "Cherry" : 3 , 
    "Dragon fruit" : 4 ,
    "Egg fruit" : 5 ,
    "Fig" : 6 ,
    "Grapes" : 7 ,
    "Hazelnut" : 8 ,
    "Ice apple" : 9 ,
    "Jack fruit" : 10
} 
print( "Values in the Dictionary:" )
for value in fruits_dictionary.values() :
    print(value)